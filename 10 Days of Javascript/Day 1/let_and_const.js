function main() {
  // Write your code here. Read input using 'readLine()' and print output using 'console.log()'.
  let r = readLine();
  const p = Math.PI;
  const area = p * r ** 2;
  const perimeter = 2 * p * r;

  // Print the area of the circle:
  console.log(area);

  // Print the perimeter of the circle:
  console.log(perimeter);
}
